class Solution
{
public:
    vector<pair<int, int>> findPairsWithGivenSum(Node *head, int target)
    {
        vector<pair<int,int>> ans;
        Node* h=head;
        Node* t=head;
        while(t->next!=NULL)t=t->next;
        while(h && t){
            if(h==t) return ans;
            int sum=h->data+t->data;
            if(sum==target){
                pair<int,int> p1;
                p1.first=h->data;
                p1.second=t->data;
                ans.push_back(p1);
                h=h->next;
                if(h==t)return ans;
                t=t->prev;
                // cout<<"f"<<endl;
            }
            else if(sum<target)h=h->next;
            else t=t->prev;
        }
        return ans;
    }
    
};

